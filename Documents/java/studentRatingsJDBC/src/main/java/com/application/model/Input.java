package com.application.model;

public class Input {
	
	
	private String type;
	private String inputText;

	public String getInput() {
		return inputText;
	}

	public void setInput(String input) {
		this.inputText = input;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
